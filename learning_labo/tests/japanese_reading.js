    function q(type, title, lines, questionHtml, answer, choices) {
        return { type, title, lines, questionHtml, answer, choices };
    }

    function shuffle(items) {
        const list = [...items];
        for (let i = list.length - 1; i > 0; i--) {
            const j = Math.floor(Math.random() * (i + 1));
            [list[i], list[j]] = [list[j], list[i]];
        }
        return list;
    }

    function takeMany(pool, count) {
        return shuffle(pool).slice(0, count);
    }

    function makePrompt(item) {
        const passageHtml = item.lines.map((line) => (
            `<span class="reading-line">${line}</span>`
        )).join("");

        return `
            <span class="question-title">${item.title}</span>
            <span class="reading-card">
                <span class="reading-passage">
                    <span class="reading-label">ぶんしょう</span>
                    ${passageHtml}
                </span>
                <span class="reading-question">
                    <span class="reading-label">もんだい</span>
                    ${item.questionHtml}
                </span>
            </span>
        `;
    }

    function cloneQuestion(item) {
        return {
            type: item.type,
            title: item.title,
            promptHtml: makePrompt(item),
            answer: item.answer,
            choices: shuffle(item.choices)
        };
    }

